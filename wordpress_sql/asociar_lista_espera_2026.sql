-- -----------------------------------------------------------------------------
-- PASO 1 — Vista previa: jugadores de la lista que YA tienen sp_current_team
-- (para detectar posibles conflictos antes de insertar)
-- -----------------------------------------------------------------------------
SELECT
    p.ID,
    p.post_title         AS Nombre,
    pm.meta_key,
    pm.meta_value        AS equipo_actual_id
FROM wp_posts p
INNER JOIN wp_postmeta pm
    ON pm.post_id = p.ID
   AND pm.meta_key = 'sp_current_team'
WHERE p.ID IN (
    21521,14219,18104,21519,4649,21536,2379,21526,18683,20075,
    2504,21527,20919,2302,4819,11608,10422,15744,20099,21537,
    5080,2226,11616,2406,20045,2531,20102,2227,18658,12603,
    4848,2438,21535,17269,20106,21528,10234,2544,17285,14343,
    15754,11628,20114,20122,21533,2532,2565,11633,2217,20138,
    2295,21522,2407,21538,2542,2240,15763,21525,10706,21540,
    2410,15765
)
ORDER BY p.post_title;


